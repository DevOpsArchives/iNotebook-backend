const mongo = require("mongoose");
const { Schema } = mongo;

const UserSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

const User = mongo.model("user", UserSchema);
User.createIndexes();
module.exports = User;
